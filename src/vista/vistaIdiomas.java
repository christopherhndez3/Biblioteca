    public void mostrarTabla() {
        limpiarTabla();
        MySQLManager manager = new MySQLManager("localhost", "3306", "bibliotecafastdevelopment", "root", "");
        manager.executeQueryConsultaLLenaTablaIdioma(listaIdiomas);
        String matriz[][] = new String[listaIdiomas.size()][2];

        for (int i = 0; i < listaIdiomas.size(); i++) {

            String idIdioma = Integer.toString(listaIdiomas.get(i).getId_idioma());

            matriz[i][0] = idIdioma;
            matriz[i][1] = listaIdiomas.get(i).getNom_idioma();

        }
        idiomaTBL.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]{
                    "Id de idioma", "Idioma"
                }
        ));

    }
    
    public void limpiarTabla() {
        for (int i = 0; i < listaIdiomas.size(); i++) {
            listaIdiomas.remove(i);
        }
               
    }
    
